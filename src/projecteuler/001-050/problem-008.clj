;; Read a "string" of digits and convert them to a sequence
(defn product [in-data]
  (reduce * (map read-string (re-seq #"\d" in-data))))


;; Return the 13 digits between idx and idx+13, inclusive.
(defn get13 [idx]
  (subs data idx (+ idx 13)))


(defn problem008 []
  (apply max  (for [x (range 0 (- 1000 13))
                    :let [ans (product (get13 x))]]
                ans)))

